import glfw 
from OpenGL.GL import*



def cubo_func():
    vertices = [
        [-0.5, -0.5, -0.5],
        [0.5, -0.5, -0.5],
        [0.5, 0.5, -0.5],
        [-0.5, 0.5, -0.5],
        [-0.5, -0.5, 0.5],
        [0.5, -0.5, 0.5],
        [0.5, 0.5, 0.5],
        [-0.5, 0.5, 0.5],
    ]
    faces = [
        [0, 1, 2, 3],
        [4, 5, 6, 7],
        [0, 1, 5, 4],
        [2, 3, 7, 6],
        [0, 3, 7, 4],
        [1, 2, 6, 5],
    ]

    cores = [
        [1.0, 0.0, 0.0],
        [0.365, 0.427, 0.882],
        [0.490, 0.604, 0.573],
        [0.957, 0.816, 0.247],
        [0.953, 0.612, 0.071],
        [0.608, 0.349, 0.712],
        [0.740, 0.765, 0.780],
        [0.105, 0.737, 0.612]
    ]

    glBegin(GL_QUADS)
    for face in faces: 
        for vertice in face:
            glColor3fv(cores[vertice])
            glVertex3fv(vertices[vertice])
    glEnd()




    